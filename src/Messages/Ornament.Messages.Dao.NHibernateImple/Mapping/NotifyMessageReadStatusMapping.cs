using FluentNHibernate.Mapping;
using Ornament.Messages.Notification;

namespace Ornament.Messages.Dao.NHibernateImple.Mapping
{
    public class MessageReadStatusMapping : ClassMap<Reader>
    {

        public MessageReadStatusMapping()
        {

            this.Table("Msgs_NotifyReader");
            this.CompositeId()
                .KeyReference(x => x.Member)
                .KeyReference(x => x.NotifyMessage);
            this.Map(x => x.Status);
        }
    }
}